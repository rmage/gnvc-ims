package com.app.wms.engine.db.exceptions;

public class CorporateDaoException extends DaoException
{
	/**
	 * Method 'CorporateDaoException'
	 * 
	 * @param message
	 */
	public CorporateDaoException(String message)
	{
		super(message);
	}

	/**
	 * Method 'CorporateDaoException'
	 * 
	 * @param message
	 * @param cause
	 */
	public CorporateDaoException(String message, Throwable cause)
	{
		super(message, cause);
	}


}
